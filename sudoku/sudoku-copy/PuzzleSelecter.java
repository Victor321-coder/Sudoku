    private static int[][] copyBoard(int[][] original){
        int[][] copy=new int[original.length][original[0].length];
        for (int row = 0; row < original.length; row++) {
            for (int col = 0; col < original[row].length; col++) {
                copy[row][col] = original[row][col];
            }
        }
    
        return copy;
        
    }
    
    
